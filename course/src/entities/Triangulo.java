package entities;

public class Triangulo {
	private double a;
	private double b;
	private double c;
	
	public Double getA () {
		return a;
	}
	
	public void setA(double a) {
		this.a = a;
	}

	public Double getB () {
		return b;
	}
	
	public void set(double b) {
		this.b = b;
	}
	
	public Double getC () {
		return c;
	}
	
	public void setB(double c) {
		this.c = c;
	}
	
}

